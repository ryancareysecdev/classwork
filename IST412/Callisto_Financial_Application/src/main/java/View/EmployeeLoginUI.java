    private void LoginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginBtnActionPerformed
        
        if (credentialCheck.verifyCredentials(UsernameTxtField.getText(), jPasswordEmpLogin.getPassword()) == true) {
            
            for (Employee employee : employees) {
                if (UsernameTxtField.getText().equals(employee.getFirstName() + employee.getLastName())) {
                    System.out.print("Welcome " + employee.getFullName() + "\nID: "
                    + employee.getId() + "\n");
                }
            }
            this.hide();
            try {
                appDriver.displayEmployeeOptions();
            } catch (InterruptedException ex) {
                Logger.getLogger(EmployeeLoginUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            System.out.println("Invalid Credentials!");
        }
    }//GEN-LAST:event_LoginBtnActionPerformed

