    private void jPasswordEmpLoginKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jPasswordEmpLoginKeyTyped
        int passStrength = 0;
        boolean hasLetter = false, hasUpper = false, hasLower = false, hasDigits = false, longEnough = false;
        char[] password = jPasswordEmpLogin.getPassword();
        jTextEmployeePwdFeedback.setText("");
        if (Arrays.toString(password).length() >= 15)
            longEnough = true;
        for (int charAt = 0; charAt < password.length; charAt++) {
            char checkMe = password[charAt];
            if (Character.isLetter(checkMe)) {
                hasLetter = true;
                if (Character.isUpperCase(checkMe)) {
                    hasUpper = true;
                }
                else if (Character.isLowerCase(checkMe)) {
                    hasLower = true;
                }
            }
            else if (Character.isDigit(checkMe)) { 
                hasDigits = true;
            }
        }
        passStrength = (hasLetter ? 1 : 0) + (hasUpper ? 1 : 0)+ (hasLower ? 1 : 0) + (hasDigits ? 1 : 0) + (longEnough ? 1 : 0);
        jProgressEmployeePwdMeter.setValue(passStrength);
        if ((hasLetter ? 1:0) + (hasUpper ? 1 : 0) + (hasLower ? 1 : 0) < 3)
            jTextEmployeePwdFeedback.append("Upper and lowercase required.\n");
        if (!hasDigits)
            jTextEmployeePwdFeedback.append("Minimum 1 digit required.\n");
        if (!longEnough)
            jTextEmployeePwdFeedback.append("Password too short.\n");      }//GEN-LAST:event_jPasswordEmpLoginKeyTyped

